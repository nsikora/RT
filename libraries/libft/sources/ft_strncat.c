#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	size_t	len;
	size_t	i;

	len = ft_strlen(dest);
	i = 0;
	while (*src && i < n)
		*(dest + len + i++) = *(src++);
	*(dest + len + i) = '\0';
	return (dest);
}
