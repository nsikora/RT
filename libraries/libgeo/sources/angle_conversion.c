#include "geometry.h"

t_dec	ft_rad_to_deg(const t_dec rad)
{
	return (rad * (180.0 / M_PI));
}

t_dec	ft_deg_to_rad(const t_dec deg)
{
	return (deg * (M_PI / 180.0));
}
